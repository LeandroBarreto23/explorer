console.log( (2 + 5) * 10)

console.log( 3 > 2 > 1 ) // 3 maior que 2? true. true maior que 1? false
console.log( 3 > 2 && 2 > 1 ) // 3 maior que 2? true. 2 maior que 1? true. true + true = true
