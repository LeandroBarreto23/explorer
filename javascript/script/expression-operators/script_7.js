// operadores logicos (logical operators)

// 2 valores booleanos que quando verificados resultará em verdadeiro ou falso

let pao = true
let queijo = true

// AND "&&" (precisa atender as duas condiçoes para resultar em verdadeiro, caso contrario retornará falso)
console.log(pao && queijo)

// OR "||" (se atender qualquer uma das duas condições resulta em verdadeiro, se nao cumprir nenhuma resulta em falso)
console.log(pao || queijo)

// NOT "!" (operador de negacao, troca o valor da variavel, se a variavel for falsa ela vira verdadeira e vice versa)
console.log(!pao)